package org.copdai.android.sensors.agent;

public class DeviceAcceleration {
    public float x;
    public float y;
    public float z;

    public DeviceAcceleration() {
    }

    public DeviceAcceleration(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
