package org.copdai.android.sensors.agent;

public class DeviceAngularAcceleration {
    public float x;
    public float y;
    public float z;

    public DeviceAngularAcceleration() {
    }

    public DeviceAngularAcceleration(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
